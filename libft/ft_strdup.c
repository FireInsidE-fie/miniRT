#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*a;

	i = 0;
	a = malloc(ft_strlen(s1) + 1);
	if (!a)
		return (NULL);
	while (s1[i])
	{
		a[i] = s1[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
